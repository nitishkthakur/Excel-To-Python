"""CodeEmitter: Assemble final Python script from generated code."""

from typing import List, Dict
from datetime import datetime
from excel_pipeline.layer4a.mapping_reader import CellMetadata
from excel_pipeline.layer4a.formula_translator import HELPER_FUNCTIONS_CODE
from excel_pipeline.utils.logging_setup import get_logger

logger = get_logger(__name__)


class CodeEmitter:
    """Assemble and emit final Python script."""

    def __init__(self):
        """Initialize CodeEmitter."""
        self.code_sections: Dict[str, List[str]] = {
            'imports': [],
            'helpers': [],
            'input_loading': [],
            'calculations': [],
            'output_writing': [],
        }

    def add_imports(self):
        """Add necessary imports."""
        self.code_sections['imports'] = [
            "#!/usr/bin/env python3",
            '"""',
            "Auto-generated by Layer 4a: Python Code Generator",
            "DO NOT EDIT - Regenerate from mapping_report.xlsx",
            "",
            f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
            "",
            "Usage: python unstructured_calculate.py",
            '"""',
            "",
            "import pandas as pd",
            "import numpy as np",
            "from openpyxl import load_workbook, Workbook",
            "from openpyxl.styles import Font, PatternFill, Alignment",
            "from openpyxl.utils import get_column_letter",
            "from datetime import datetime, date",
            "import math",
            "",
        ]

    def add_helper_functions(self):
        """Add helper functions for Excel function emulation."""
        self.code_sections['helpers'] = [
            "# ========== HELPER FUNCTIONS ==========",
            "",
            HELPER_FUNCTIONS_CODE,
            "",
        ]

    def add_input_loading(self, input_path: str = "unstructured_inputs.xlsx"):
        """
        Add code to load inputs from Excel file.

        Args:
            input_path: Path to input Excel file
        """
        self.code_sections['input_loading'] = [
            "def load_inputs(input_path):",
            '    """Load input values from Excel file."""',
            '    print(f"Loading inputs from {input_path}...")',
            "    c = {}  # Cell store: (sheet, column, row) -> value",
            "",
            '    wb_input = load_workbook(input_path, data_only=True)',
            "",
            "    for sheet in wb_input.worksheets:",
            "        for row in sheet.iter_rows():",
            "            for cell in row:",
            "                if cell.value is not None:",
            "                    c[(sheet.title, cell.column_letter, cell.row)] = cell.value",
            "",
            '    print(f"  Loaded {len(c)} input cells")',
            "    return c",
            "",
        ]

    def add_calculation_section(self, code_lines: List[str]):
        """
        Add calculation code section.

        Args:
            code_lines: List of Python code lines for calculations
        """
        if not self.code_sections['calculations']:
            self.code_sections['calculations'] = [
                "def calculate(c):",
                '    """Perform all calculations."""',
                '    print("Calculating formulas...")',
                "",
            ]

        # Indent code lines
        indented = ["    " + line if line else "" for line in code_lines]
        self.code_sections['calculations'].extend(indented)

    def add_output_writing(self, cells: List[CellMetadata], output_path: str = "output.xlsx"):
        """
        Add code to write output to Excel file.

        Args:
            cells: List of all cells to write
            output_path: Path to output Excel file
        """
        self.code_sections['output_writing'] = [
            "def write_output(c, output_path):",
            '    """Write calculated values to output Excel file."""',
            '    print(f"Writing output to {output_path}...")',
            "",
            "    wb_out = Workbook()",
            "    if 'Sheet' in wb_out.sheetnames:",
            "        wb_out.remove(wb_out['Sheet'])",
            "",
            "    # Group cells by sheet",
            "    cells_by_sheet = {}",
            "    for (sheet, col, row), value in c.items():",
            "        if sheet not in cells_by_sheet:",
            "            cells_by_sheet[sheet] = []",
            "        cells_by_sheet[sheet].append((col, row, value))",
            "",
            "    # Create sheets and write cells",
            "    for sheet_name, sheet_cells in cells_by_sheet.items():",
            "        ws = wb_out.create_sheet(title=sheet_name)",
            "",
            "        for col, row, value in sheet_cells:",
            "            cell = ws[f'{col}{row}']",
            "            cell.value = value",
            "",
            "            # TODO: Apply formatting from metadata",
            "",
            "    wb_out.save(output_path)",
            '    print(f"  Wrote {len(c)} cells to {output_path}")',
            "",
        ]

    def add_main_function(self):
        """Add main() function that orchestrates execution."""
        main_code = [
            "",
            "# ========== MAIN ==========",
            "",
            "def main():",
            '    """Main execution function."""',
            '    print("=" * 60)',
            '    print("Excel-to-Python: Unstructured Calculator")',
            '    print("=" * 60)',
            "",
            "    # Load inputs",
            '    c = load_inputs("unstructured_inputs.xlsx")',
            "",
            "    # Calculate formulas",
            "    calculate(c)",
            "",
            "    # Write output",
            '    write_output(c, "output.xlsx")',
            "",
            '    print("=" * 60)',
            '    print(f"Done! Calculated {len(c)} cells.")',
            '    print("=" * 60)',
            "",
            "",
            'if __name__ == "__main__":',
            "    main()",
            "",
        ]

        self.code_sections['main'] = main_code

    def assemble(self) -> List[str]:
        """
        Assemble all code sections into final script.

        Returns:
            List of all code lines
        """
        all_lines = []

        # Add each section in order
        for section in ['imports', 'helpers', 'input_loading', 'calculations', 'output_writing']:
            if section in self.code_sections:
                all_lines.extend(self.code_sections[section])

        # Add main function if not already added
        if 'main' not in self.code_sections:
            self.add_main_function()

        all_lines.extend(self.code_sections.get('main', []))

        return all_lines

    def write_to_file(self, output_path: str):
        """
        Write assembled code to file.

        Args:
            output_path: Path to write Python script
        """
        logger.info(f"Writing generated code to: {output_path}")

        all_lines = self.assemble()

        with open(output_path, 'w') as f:
            for line in all_lines:
                f.write(line + '\n')

        logger.info(f"Generated script: {len(all_lines)} lines")

    def get_code_stats(self) -> Dict:
        """
        Get statistics about generated code.

        Returns:
            Dictionary with statistics
        """
        all_lines = self.assemble()

        stats = {
            'total_lines': len(all_lines),
            'import_lines': len(self.code_sections.get('imports', [])),
            'helper_lines': len(self.code_sections.get('helpers', [])),
            'calculation_lines': len(self.code_sections.get('calculations', [])),
            'output_lines': len(self.code_sections.get('output_writing', [])),
        }

        return stats
